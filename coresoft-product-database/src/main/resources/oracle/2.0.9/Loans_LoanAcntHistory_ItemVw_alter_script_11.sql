create or replace view LOAN_ACCOUNT_HISTORY_ITEM_VW
as
SELECT
    TRANSACTION_ID,
    AMOUNT,
    ACCOUNT_ID,
    DESCRIPTION,
    POST_DATE,
    EFFECTIVE_DATE,
    TRAN_CODE,
    INSTITUTION_ID,
    LOAN_ENTRY_TYPE_ID,
    EVENT_TYPE_ID,
    ENTRY_DATE,
    PARENT_TRANSACTION_ID,
    TRANSACTION_DETAILS,
    STMT_DESC,
    EVENT_ID,
    SEQUENCE_NO
FROM
    LOAN_ACCOUNT_HISTORY_ITEM
WHERE
    TRAN_CODE IN ('1801', '1802', '1803', '1804', '1805', '1806', '1809', '1836', '1901', '1902', '1903', '1904', '1905', '1906', '1909', '1936', '1950', '1850','1990','1890');